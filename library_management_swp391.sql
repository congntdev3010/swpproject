-- 5. Book Authors
CREATE TABLE book_authors (
    book_id   INT  NOT NULL,
    author_id INT  NOT NULL,
    role      ENUM ('PRIMARY', 'CO_AUTHOR', 'TRANSLATOR', 'EDITOR') DEFAULT 'PRIMARY' NOT NULL,
    PRIMARY KEY (book_id, author_id),
    CONSTRAINT fk_ba_author FOREIGN KEY (author_id) REFERENCES authors (id) ON UPDATE CASCADE,
    CONSTRAINT fk_ba_book   FOREIGN KEY (book_id)   REFERENCES books (id)   ON UPDATE CASCADE ON DELETE CASCADE
