-- 8. Book Reservations
CREATE TABLE book_reservations (
    id           INT AUTO_INCREMENT PRIMARY KEY,
    book_id      INT      NOT NULL,
    user_id      INT      NOT NULL,
    reserve_date DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    expiry_date  DATETIME NULL,
    status       ENUM ('PENDING', 'READY', 'COMPLETED', 'CANCELLED', 'EXPIRED') DEFAULT 'PENDING' NOT NULL,
    notified_at  DATETIME NULL,
    created_at   DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at   DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_reservations_book FOREIGN KEY (book_id) REFERENCES books (id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_reservations_user FOREIGN KEY (user_id) REFERENCES users (id) ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE INDEX idx_reservations_book   ON book_reservations (book_id);
CREATE INDEX idx_reservations_user   ON book_reservations (user_id);
CREATE INDEX idx_reservations_status ON book_reservations (status);

-- 9. Book Reviews
CREATE TABLE book_reviews (
    id         INT AUTO_INCREMENT PRIMARY KEY,
    book_id    INT     NOT NULL,
    user_id    INT     NOT NULL,
    rating     TINYINT NOT NULL,
    comment    TEXT    NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT uq_review_book_user UNIQUE (book_id, user_id),
    CONSTRAINT fk_reviews_book     FOREIGN KEY (book_id) REFERENCES books (id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_reviews_user     FOREIGN KEY (user_id) REFERENCES users (id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT chk_rating          CHECK ((`rating` >= 1) AND (`rating` <= 5))
);
CREATE INDEX idx_reviews_book ON book_reviews (book_id);
CREATE INDEX idx_reviews_user ON book_reviews (user_id);

-- 10. Borrow Records
-- Thay Äá»i:
--   [+] ThÃªm copy_id FK -> book_copies (theo dÃµi báº£n sao cá»¥ thá» ÄÆ°á»£c mÆ°á»£n)
--   [+] ThÃªm renewal_count INT (Äáº¿m sá» láº§n gia háº¡n)
CREATE TABLE borrow_records (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    user_id       INT  NOT NULL,
    book_id       INT  NOT NULL,
    copy_id       INT  NULL COMMENT 'Báº£n sao váº­t lÃ½ cá»¥ thá» ÄÆ°á»£c mÆ°á»£n',
    borrow_date   DATE NOT NULL,
    due_date      DATE NOT NULL,
    return_date   DATE NULL,
    renewal_count INT  DEFAULT 0 NOT NULL COMMENT 'Sá» láº§n ÄÃ£ gia háº¡n',
    status        ENUM ('BORROWING', 'RETURNED', 'OVERDUE', 'LOST') DEFAULT 'BORROWING' NOT NULL,
    note          TEXT NULL,
    created_at    DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at    DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_borrow_book FOREIGN KEY (book_id) REFERENCES books (id)       ON UPDATE CASCADE,
    CONSTRAINT fk_borrow_user FOREIGN KEY (user_id) REFERENCES users (id)       ON UPDATE CASCADE,
    CONSTRAINT fk_borrow_copy FOREIGN KEY (copy_id) REFERENCES book_copies (id) ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT chk_renewal    CHECK (`renewal_count` >= 0)
);
CREATE INDEX idx_borrow_user   ON borrow_records (user_id);
CREATE INDEX idx_borrow_book   ON borrow_records (book_id);
CREATE INDEX idx_borrow_copy   ON borrow_records (copy_id);
CREATE INDEX idx_borrow_due    ON borrow_records (due_date);
CREATE INDEX idx_borrow_status ON borrow_records (status);

-- 11. Fines
CREATE TABLE fines (
    id                INT AUTO_INCREMENT PRIMARY KEY,
    borrow_record_id  INT            NOT NULL,
    user_id           INT            NOT NULL,
    amount            DECIMAL        DEFAULT 0    NOT NULL,
    overdue_days      INT            DEFAULT 0    NOT NULL,
    reason            VARCHAR(500)   NULL,
    status            ENUM ('UNPAID', 'PENDING_VERIFY', 'PAID', 'WAIVED') DEFAULT 'UNPAID' NOT NULL,
    payment_method    ENUM ('CASH', 'ONLINE')                             DEFAULT 'CASH'   NOT NULL,
    payment_note      VARCHAR(255)   NULL,
    paid_date         DATE           NULL,
    created_at        DATETIME       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at        DATETIME       DEFAULT CURRENT_TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_fines_borrow FOREIGN KEY (borrow_record_id) REFERENCES borrow_records (id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_fines_user   FOREIGN KEY (user_id)          REFERENCES users (id)          ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE INDEX idx_fines_borrow ON fines (borrow_record_id);
CREATE INDEX idx_fines_user   ON fines (user_id);
CREATE INDEX idx_fines_status ON fines (status);

-- 12. Notifications
CREATE TABLE notifications (
    id             INT AUTO_INCREMENT PRIMARY KEY,
    user_id        INT          NOT NULL,
    title          VARCHAR(255) NOT NULL,
    message        TEXT         NOT NULL,
    type           ENUM ('DUE_REMINDER', 'OVERDUE', 'FINE', 'RESERVATION', 'SYSTEM') DEFAULT 'SYSTEM' NOT NULL,
    is_read        TINYINT(1)   DEFAULT 0 NOT NULL,
    reference_id   INT          NULL,
    reference_type VARCHAR(50)  NULL,
    created_at     DATETIME     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_notif_user FOREIGN KEY (user_id) REFERENCES users (id) ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE INDEX idx_notif_user    ON notifications (user_id);
CREATE INDEX idx_notif_read    ON notifications (is_read);
CREATE INDEX idx_notif_created ON notifications (created_at);

-- 13. User Memberships
CREATE TABLE user_memberships (
    id                  INT AUTO_INCREMENT PRIMARY KEY,
    user_id             INT  NOT NULL,
    tier_id             INT  NOT NULL,
    total_books_borrowed INT  DEFAULT 0 NOT NULL,
    tier_achieved_at    DATE DEFAULT (CURDATE()) NOT NULL,
    updated_at          DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT uq_um_user  UNIQUE (user_id),
    CONSTRAINT fk_um_user  FOREIGN KEY (user_id)  REFERENCES users (id)            ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_um_tier  FOREIGN KEY (tier_id)  REFERENCES membership_tiers (id) ON UPDATE CASCADE
);

-- 14. User Tokens
CREATE TABLE user_tokens (
    id         INT AUTO_INCREMENT PRIMARY KEY,
    user_id    INT          NOT NULL,
    token      VARCHAR(255) NOT NULL,
    type       ENUM ('REGISTRATION', 'RESET_PASSWORD') NOT NULL,
    expiry     DATETIME     NOT NULL,
    created_at DATETIME     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT uq_user_token    UNIQUE (token),
    CONSTRAINT fk_token_user    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);
CREATE INDEX idx_user_tokens_token ON user_tokens (token);
CREATE INDEX idx_user_tokens_user  ON user_tokens (user_id);

